namespace Example_PangYa.Classes
{
    internal class MOBA
    {
        private string name = null;
        private string serialNo = null;
        private string marca = null;

        public string Name
        {
            get { return name; }
            set { name = value; }
        }
        public string SerialNo
        {
            get { return serialNo; }
            set { serialNo = value; }
        }
        public string Marca
        {
            get { return marca; }
            set { marca = value; }
        }
    }
}
